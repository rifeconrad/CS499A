WorldMap::WorldMap()
{
	this->dirt = TextureManager::New("../../Sprites/Map/dirt.png");
	this->grass = TextureManager::New("../../Sprites/Map/grass.png");
	this->water = TextureManager::New("../../Sprites/Map/water.png");

	this->loadMap(lvl1);

	src.x = 0;
	src.y = 0;

	src.w = 32;
	src.h = 32;

	dst.w = 32;
	dst.h = 32;

	dst.x = 0;
	dst.y = 0;
}


WorldMap::~WorldMap()
{
}

void WorldMap::loadMap(int map[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			this->map[row][col] = map[row][col];
		}
	}
}
#include <iostream>
void WorldMap::render()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			type = this->map[row][col];

			dst.x = col * 32;
			dst.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(this->water, this->src, this->dst);
				break;
			case 1:
				TextureManager::Draw(this->dirt, this->src, this->dst);
				break;
			case 2:
				TextureManager::Draw(this->grass, this->src, this->dst);
				break;
			default:
				break;
			};
		}
	}
}
